#!/usr/bin/env node
/**
 * Generates packages/shared/src/schemas/hugeicons-free-icons.ts from @hugeicons/core-free-icons.
 * Run: node scripts/generate-hugeicons-names.mjs
 */
import { writeFileSync } from 'node:fs'
import { dirname, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = dirname(fileURLToPath(import.meta.url))
const root = resolve(__dirname, '..')
const iconsPackageRoot = resolve(root, 'apps/web/node_modules/@hugeicons/core-free-icons/dist/esm/index.js')

const iconsModule = await import(iconsPackageRoot)
const iconNames = Object.keys(iconsModule)
  .filter((key) => key.endsWith('Icon'))
  .sort()

const outputPath = resolve(root, 'packages/shared/src/schemas/hugeicons-free-icons.ts')
const content = `// Auto-generated by scripts/generate-hugeicons-names.mjs — do not edit manually.
// Re-run after upgrading @hugeicons/core-free-icons.

/** All icon export names from @hugeicons/core-free-icons (${iconNames.length} icons). */
export const HUGEICONS_FREE_ICON_NAMES = ${JSON.stringify(iconNames, null, 2)} as const

export type HugeiconsFreeIconName = (typeof HUGEICONS_FREE_ICON_NAMES)[number]

const hugeiconsFreeIconNameSet = new Set<string>(HUGEICONS_FREE_ICON_NAMES)

/** Validates that a string is a known free Hugeicons icon name. */
export function isHugeiconsFreeIconName(value: string): value is HugeiconsFreeIconName {
  return hugeiconsFreeIconNameSet.has(value)
}
`

writeFileSync(outputPath, content, 'utf8')
console.log(`Generated ${iconNames.length} icon names → ${outputPath}`)
